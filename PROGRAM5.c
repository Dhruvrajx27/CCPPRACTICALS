// Documentation : Convert The temperature Celsius to Fahrenheit
// Header File.
#include<stdio.h>

int main(){

    float  f , c; // c is Celsius.

    // Take a Input As Celsius.
    printf(" Enter a Temperature :");
    scanf("%f", &c);

    //Process Of Convert Celsius To Fahrenheit
     f = ((9*c)/5)+32;

    //Output As Fahrenheit Temperature.
    printf("\n Temperature In Fahrenheit: %f F\n\n" ,f);   // f is Fahrenheit
    printf(" 24CE019_DHRUVRAJ");

    return 0;
}

